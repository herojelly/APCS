public interface Point {
	/**
	 * Gets x-coordinate
	 * @return x-coordinate
	 */
	public int getX();

	/**
	 * Sets x-coordinate
	 * @param x-coordinate
	 */
	public void setX(int x);
	
	/**
	 * Gets y-coordinate
	 * @return y-coordinate
	 */
	public int getY();
	
	/**
	 * Sets y-coordinate
	 * @param y-coordinate
	 */
	public void setY(int y);
}
